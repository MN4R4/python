p = int(input("Digite o peso do lutador: "))
if p < 65:
    print("Pena")
elif p > 65 and p < 72:
    print("Leve")
elif p > 72 and p < 79:
    print("Ligeiro")
elif p > 79 and p < 86:
    print("Meio-Medio")
elif p > 86 and p < 93:
    print  ("Medio")
elif p > 93 and p < 100:
    print  ("Meio-Pesado")
else:
    print  ("Pesado")




        
